#include "../src/aes.h"
#include <stdio.h>
#include <string.h>

int main() {

    uint8_t iv[16] = { 0x00, 0x01, 0x02, 0x03,
                       0x04, 0x05, 0x06, 0x07,
                       0x08, 0x09, 0x0a, 0x0b,
                       0x0c, 0x0d, 0x0e, 0x0f };

    uint8_t key[24] = { 0x8e, 0x73, 0xb0, 0xf7,
                        0xda, 0x0e, 0x64, 0x52,
                        0xc8, 0x10, 0xf3, 0x2b,
                        0x80, 0x90, 0x79, 0xe5,
                        0x62, 0xf8, 0xea, 0xd2,
                        0x52, 0x2c, 0x6b, 0x7b };

    uint8_t input[64] = { 0x3b, 0xe9, 0x5a, 0x57,
                          0xcb, 0x1f, 0x39, 0x1a,
                          0x65, 0xb6, 0x93, 0xb5,
                          0x97, 0x0c, 0xbc, 0x7b,
                          0xbd, 0xc4, 0xff, 0x45,
                          0x6b, 0xde, 0xc5, 0x00,
                          0xa3, 0x7b, 0x2b, 0x38,
                          0x84, 0xdf, 0xc5, 0xdb,
                          0x74, 0xeb, 0x68, 0xa1,
                          0xa0, 0xb2, 0x1e, 0x1a,
                          0xbe, 0x80, 0x6e, 0xdb,
                          0xd2, 0x24, 0x09, 0x9e,
                          0x6f, 0xcf, 0xf3, 0xde,
                          0x08, 0xff, 0x39, 0xe7,
                          0x18, 0xe3, 0x54, 0xd1,
                          0x0f, 0xa9, 0xf6, 0x5d };

    uint8_t source[64];
    memcpy(source, input, 64);

    uint8_t expected[64] = { 0x9a, 0x9d, 0x9f, 0x2c,
                            0x0c, 0x05, 0x92, 0xf4,
                            0xf3, 0x89, 0x24, 0xf9,
                            0x44, 0x45, 0x31, 0x2a,
                            0xcd, 0xce, 0x31, 0xac,
                            0x9b, 0xaf, 0xdc, 0x56,
                            0x08, 0x45, 0xe2, 0x61,
                            0x4e, 0x05, 0x60, 0xf4,
                            0x83, 0x81, 0xf6, 0x4b,
                            0x12, 0x1b, 0xc9, 0x12,
                            0xe8, 0xf2, 0x4d, 0x2c,
                            0xfa, 0x97, 0x55, 0xf3,
                            0x56, 0x8e, 0x55, 0x37,
                            0x49, 0xf1, 0x45, 0x4c,
                            0xec, 0x78, 0x97, 0x7a,
                            0xf3, 0x24, 0x0f, 0xf4 };

    cbc_encrypt(input, key, 192, iv, 64);

    for (int i = 0; i < 63; i++) {
        {
            if (input[i] != expected[i]) {
                printf("Error at index %d\n", i);
                printf("Expected: %x\n", expected[i]);
                printf("Got: %x\n", input[i]);
                return 1;
            }
        }
    }

    cbc_decrypt(input, key, 192, iv, 64);

    for (int i = 0; i < 63; i++) {
        {
            if (input[i] != source[i]) {
                printf("Error at index %d\n", i);
                printf("Expected: %x\n", source[i]);
                printf("Got: %x\n", input[i]);
                return 1;
            }
        }
    }
    return 0;
}